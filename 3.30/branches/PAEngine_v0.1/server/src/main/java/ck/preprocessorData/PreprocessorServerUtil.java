package ck.preprocessorData;

/**
 * <h3>预处理工具类</h3>
 * 
 * @author LIUJUNWU
 * @version 1.0.0
 */
public class PreprocessorServerUtil {
	/**
	 *  返回规则类型 1为配伍禁忌，2为相互作用，3为重复用药
	 */
	public static String getTypeName(Integer type) {
		switch (type) {
		case 1:
			return "配伍禁忌";
		case 2:
			return "相互作用";
		case 3:
			return "重复用药";
		default:
			return null;
		}
	}
}